"""Builds and writes the PDF pitch report using fpdf2, assembling all analysis sections."""

from fpdf import FPDF
from analysis.valuation import compute_ebitda


class PitchOSReport(FPDF):
    """A4 portrait PDF report with branded header/footer and structured section layout."""

    # Brand palette
    NAVY      = (26, 43, 74)       # #1a2b4a -- header bar
    WHITE     = (255, 255, 255)
    GREY_BG   = (232, 232, 232)    # section title band
    GREY_TEXT = (120, 120, 120)    # footer / muted labels
    DARK_TEXT = (40, 40, 40)

    # Column widths for two-column tables (left label + right value)
    COL_LABEL = 55
    COL_VALUE = 125

    def __init__(self, acquirer_ticker: str, target_ticker: str) -> None:
        # 22mm top margin gives header (16mm bar) + 6mm breathing room before content
        super().__init__(orientation="P", unit="mm", format="A4")
        self.set_margins(left=15, top=22, right=15)
        self.set_auto_page_break(auto=True, margin=18)
        self._deal_label = f"{acquirer_ticker.upper()} -> {target_ticker.upper()}"

    # ── fpdf2 page hooks ──────────────────────────────────────────────────────

    def header(self) -> None:
        """Dark navy bar across the full page width with report title and deal label."""
        # Full-width navy rectangle (x=0 so it bleeds to the edge, not the margin)
        self.set_fill_color(*self.NAVY)
        self.rect(0, 0, 210, 16, style="F")

        self.set_font("Helvetica", "B", 9)
        self.set_text_color(*self.WHITE)

        # Left: report title
        self.set_xy(15, 5)
        self.cell(100, 6, "PitchOS  |  M&A Rationale Report", align="L")

        # Right: deal label (acquirer -> target)
        self.set_xy(0, 5)
        self.cell(195, 6, self._deal_label, align="R")

        # Reset text colour; cursor lands at top-margin start
        self.set_text_color(0, 0, 0)

    def footer(self) -> None:
        """Page number left, attribution right, in small grey text."""
        self.set_y(-12)
        self.set_font("Helvetica", "", 7)
        self.set_text_color(*self.GREY_TEXT)
        self.cell(0, 6, f"Page {self.page_no()}", align="L")
        self.set_y(-12)
        self.cell(0, 6, "Generated by PitchOS", align="R")
        self.set_text_color(0, 0, 0)

    # ── layout helpers ────────────────────────────────────────────────────────

    def render_section_title(self, title: str) -> None:
        """Light grey background band with bold dark section title."""
        self.set_fill_color(*self.GREY_BG)
        self.set_font("Helvetica", "B", 10)
        self.set_text_color(*self.DARK_TEXT)
        self.cell(0, 8, f"  {title}", border=0, new_x="LMARGIN", new_y="NEXT",
                  align="L", fill=True)
        self.ln(2)
        self.set_text_color(0, 0, 0)

    # ── formatting helpers ────────────────────────────────────────────────────

    @staticmethod
    def _fmt(value, prefix: str = "$", suffix: str = "") -> str:
        """Format a numeric value as $XB / $XM / $XK, or 'N/A' if None."""
        if value is None:
            return "N/A"
        v = float(value)
        if abs(v) >= 1e12:
            return f"{prefix}{v / 1e12:.2f}T{suffix}"
        if abs(v) >= 1e9:
            return f"{prefix}{v / 1e9:.2f}B{suffix}"
        if abs(v) >= 1e6:
            return f"{prefix}{v / 1e6:.2f}M{suffix}"
        return f"{prefix}{v:,.2f}{suffix}"

    @staticmethod
    def _fmt_price(value) -> str:
        """Format a per-share price as $X.XX."""
        if value is None:
            return "N/A"
        return f"${float(value):.2f}"

    def _row(self, label: str, value: str, shade: bool = False) -> None:
        """Render one label/value row in the two-column table style."""
        if shade:
            self.set_fill_color(246, 246, 246)
        else:
            self.set_fill_color(*self.WHITE)

        self.set_font("Helvetica", "", 9)
        self.set_text_color(*self.GREY_TEXT)
        self.cell(self.COL_LABEL, 7, label, border="B", new_x="RIGHT", new_y="TOP",
                  align="L", fill=True)

        self.set_font("Helvetica", "B", 9)
        self.set_text_color(*self.DARK_TEXT)
        self.cell(self.COL_VALUE, 7, value, border="B", new_x="LMARGIN", new_y="NEXT",
                  align="L", fill=True)

        self.set_text_color(0, 0, 0)

    # ── content sections ──────────────────────────────────────────────────────

    def render_company_snapshot(self, financials: dict, label: str) -> None:
        """Two-column mini table showing key financial metrics for one company."""
        name = financials.get("company_name") or financials.get("ticker", "Unknown")
        self.render_section_title(f"{label}: {name}")

        ebitda = compute_ebitda(financials)

        rows = [
            ("Company Name",  name),
            ("Ticker",        financials.get("ticker") or "N/A"),
            ("Market Cap",    self._fmt(financials.get("market_cap"))),
            ("Revenue",       self._fmt(financials.get("revenue"))),
            ("EBITDA",        self._fmt(ebitda)),
            ("Total Debt",    self._fmt(financials.get("total_debt"))),
            ("Current Price", self._fmt_price(financials.get("current_price"))),
        ]

        for i, (lbl, val) in enumerate(rows):
            self._row(lbl, val, shade=(i % 2 == 0))

        self.ln(5)

    def render_valuation_section(self, acquirer_val: dict, target_val: dict,
                                  acquirer_data: dict, target_data: dict) -> None:
        """Side-by-side EBITDA / EV / EV-EBITDA table with brief commentary."""
        self.render_section_title("Valuation Bridge  (EV / EBITDA)")

        acq_name = (acquirer_data.get("ticker") or "Acquirer").upper()
        tgt_name  = (target_data.get("ticker") or "Target").upper()

        # Header row
        self.set_fill_color(*self.NAVY)
        self.set_text_color(*self.WHITE)
        self.set_font("Helvetica", "B", 9)
        col_w = [70, 55, 55]
        self.cell(col_w[0], 7, "Metric", border=1, new_x="RIGHT", new_y="TOP",
                  align="C", fill=True)
        self.cell(col_w[1], 7, acq_name, border=1, new_x="RIGHT", new_y="TOP",
                  align="C", fill=True)
        self.cell(col_w[2], 7, tgt_name, border=1, new_x="LMARGIN", new_y="NEXT",
                  align="C", fill=True)
        self.set_text_color(0, 0, 0)

        # Data rows
        metrics = [
            ("EBITDA",
             self._fmt(acquirer_val.get("ebitda")),
             self._fmt(target_val.get("ebitda"))),
            ("Enterprise Value",
             self._fmt(acquirer_val.get("enterprise_value")),
             self._fmt(target_val.get("enterprise_value"))),
            ("EV / EBITDA Multiple",
             f"{acquirer_val.get('ev_ebitda_multiple') or 'N/A'}x"
             if acquirer_val.get("ev_ebitda_multiple") is not None else "N/A",
             f"{target_val.get('ev_ebitda_multiple') or 'N/A'}x"
             if target_val.get("ev_ebitda_multiple") is not None else "N/A"),
        ]

        for i, (metric, acq_v, tgt_v) in enumerate(metrics):
            shade = (i % 2 == 0)
            bg = (246, 246, 246) if shade else self.WHITE
            self.set_fill_color(*bg)
            self.set_font("Helvetica", "", 9)
            self.set_text_color(*self.DARK_TEXT)
            self.cell(col_w[0], 7, metric, border=1, new_x="RIGHT", new_y="TOP",
                      align="L", fill=True)
            self.set_font("Helvetica", "B", 9)
            self.cell(col_w[1], 7, acq_v, border=1, new_x="RIGHT", new_y="TOP",
                      align="C", fill=True)
            self.cell(col_w[2], 7, tgt_v, border=1, new_x="LMARGIN", new_y="NEXT",
                      align="C", fill=True)

        self.ln(4)

        # Two-line commentary
        acq_mult = acquirer_val.get("ev_ebitda_multiple")
        tgt_mult  = target_val.get("ev_ebitda_multiple")
        self.set_font("Helvetica", "I", 8)
        self.set_text_color(*self.GREY_TEXT)

        if acq_mult is not None and tgt_mult is not None:
            if tgt_mult > acq_mult:
                note = (
                    f"{tgt_name} trades at a higher EV/EBITDA multiple ({tgt_mult:.1f}x) "
                    f"than {acq_name} ({acq_mult:.1f}x), implying the deal is priced at a premium "
                    "to the acquirer's own market valuation -- synergy delivery is essential to justify the spread."
                )
            else:
                note = (
                    f"{tgt_name} trades at a lower EV/EBITDA multiple ({tgt_mult:.1f}x) "
                    f"than {acq_name} ({acq_mult:.1f}x), suggesting the acquisition could be "
                    "immediately accretive if integration costs remain contained."
                )
        else:
            note = "Insufficient data to generate EV/EBITDA commentary -- verify financials manually."

        self.multi_cell(0, 5, note, align="L")
        self.set_text_color(0, 0, 0)
        self.ln(5)

    def render_dcf_section(self, dcf_result: dict, premium: dict) -> None:
        """DCF summary table + colour-coded premium box + one-line interpretation."""
        self.render_section_title("DCF Valuation  (5-Year Projection + Terminal Value)")

        # Summary table
        dcf_rows = [
            ("PV of Free Cash Flows",   self._fmt(dcf_result.get("total_pv_fcf"))),
            ("PV of Terminal Value",    self._fmt(dcf_result.get("pv_terminal_value"))),
            ("DCF Enterprise Value",    self._fmt(dcf_result.get("enterprise_value_dcf"))),
            ("Implied Share Price",     self._fmt_price(dcf_result.get("implied_share_price"))),
        ]

        for i, (lbl, val) in enumerate(dcf_rows):
            self._row(lbl, val, shade=(i % 2 == 0))

        self.ln(5)

        # Colour-coded premium box
        is_accretive = premium.get("is_accretive")
        if is_accretive is True:
            box_color = (220, 245, 220)   # light green
            label_color = (34, 120, 34)
        elif is_accretive is False:
            box_color = (250, 220, 220)   # light red
            label_color = (160, 30, 30)
        else:
            box_color = (240, 240, 240)   # neutral grey
            label_color = (80, 80, 80)

        # Draw the filled background rectangle
        box_x = self.get_x()
        box_y = self.get_y()
        self.set_fill_color(*box_color)
        self.rect(box_x, box_y, 180, 14, style="F")

        # Overlay three values inside the box
        premium_pct   = premium.get("premium_pct")
        implied_price = self._fmt_price(dcf_result.get("implied_share_price"))
        prem_label    = premium.get("label", "N/A")
        pct_str       = f"{premium_pct:+.1f}%" if premium_pct is not None else "N/A"

        self.set_xy(box_x + 4, box_y + 3)
        self.set_font("Helvetica", "", 8)
        self.set_text_color(*self.GREY_TEXT)
        self.cell(40, 5, "DCF Implied Price", align="L")
        self.cell(40, 5, f"{prem_label} / Discount", align="L")
        self.cell(40, 5, "Premium %", align="L")

        self.set_xy(box_x + 4, box_y + 8)
        self.set_font("Helvetica", "B", 9)
        self.set_text_color(*label_color)
        self.cell(40, 5, implied_price, align="L")
        self.cell(40, 5, prem_label, align="L")
        self.cell(40, 5, pct_str, align="L")

        self.set_text_color(0, 0, 0)
        self.ln(18)

        # One-line interpretation
        self.set_font("Helvetica", "I", 8)
        self.set_text_color(*self.DARK_TEXT)
        if premium_pct is not None:
            direction = "premium" if is_accretive else "discount"
            stance    = "attractive at current terms" if is_accretive else "potentially expensive at current terms"
            interp = (
                f"DCF implies a {abs(premium_pct):.1f}% {direction} to the current market price. "
                f"Deal appears {stance}."
            )
        else:
            interp = "Insufficient data to generate DCF interpretation -- verify revenue and share count."
        self.multi_cell(0, 5, interp, align="L")
        self.set_text_color(0, 0, 0)
        self.ln(4)

    def render_rationale_summary(
        self,
        acquirer_name: str,
        target_name: str,
        valuation: dict,
        dcf: dict,
        flags: list,
    ) -> None:
        """3-4 sentence plain-English M&A rationale with analyst recommendation."""
        self.render_section_title("M&A Rationale Summary")

        ev_mult   = valuation.get("ev_ebitda_multiple")
        prem_pct  = dcf.get("premium_pct")
        flag_count = len(flags)

        # Sentence 1: deal overview with multiple
        s1 = (
            f"{acquirer_name} is evaluating an acquisition of {target_name} "
            f"at an EV/EBITDA of {ev_mult:.1f}x." if ev_mult is not None
            else f"{acquirer_name} is evaluating an acquisition of {target_name} "
                 "(EV/EBITDA unavailable -- verify financials)."
        )

        # Sentence 2: DCF premium/discount
        if prem_pct is not None:
            direction = "premium" if prem_pct >= 0 else "discount"
            s2 = f"DCF analysis implies a {abs(prem_pct):.1f}% {direction} to the current market price."
        else:
            s2 = "DCF analysis could not compute an implied premium -- revenue or share data missing."

        # Sentence 3: flag count
        s3 = (
            f"{flag_count} risk flag{'s were' if flag_count != 1 else ' was'} "
            "identified by automated screening."
        )

        # Sentence 4: analyst recommendation based on flag count
        if flag_count == 0:
            rec = "Analyst recommendation: proceed to second-stage diligence -- no automated flags raised."
        elif flag_count <= 2:
            rec = "Analyst recommendation: exercise caution -- review flagged items before proceeding."
        else:
            rec = "Analyst recommendation: full review required -- multiple risk flags warrant detailed scrutiny."

        self.set_font("Helvetica", "", 9)
        self.set_text_color(*self.DARK_TEXT)
        self.multi_cell(0, 6, f"{s1} {s2} {s3} {rec}", align="L")
        self.set_text_color(0, 0, 0)
        self.ln(4)

    def render_risk_flags(self, flags: list) -> None:
        """Amber-bulleted risk flag list, or green 'no risks' line if empty."""
        self.render_section_title("Automated Deal Risk Flags")

        if not flags:
            # Green 'all clear' line
            self.set_font("Helvetica", "B", 9)
            self.set_text_color(34, 120, 34)
            self.cell(0, 7, "  No material deal risks identified by automated screen.",
                      new_x="LMARGIN", new_y="NEXT", align="L")
            self.set_text_color(0, 0, 0)
        else:
            for flag in flags:
                row_y = self.get_y()
                row_x = self.get_x()

                # Small amber/orange filled square as bullet indicator
                self.set_fill_color(230, 140, 30)   # amber
                self.rect(row_x, row_y + 2, 3, 3, style="F")

                # Flag text, indented past the bullet
                self.set_xy(row_x + 6, row_y)
                self.set_font("Helvetica", "", 8.5)
                self.set_text_color(*self.DARK_TEXT)
                self.multi_cell(0, 6, flag, align="L")
                self.ln(1)

        self.set_text_color(0, 0, 0)
        self.ln(2)

        # Analyst disclaimer footnote
        self.set_font("Helvetica", "I", 7)
        self.set_text_color(*self.GREY_TEXT)
        self.multi_cell(
            0, 4,
            "Flags are algorithmically generated. Analyst review required before distribution.",
            align="L",
        )
        self.set_text_color(0, 0, 0)
        self.ln(4)


# ── public entry point ────────────────────────────────────────────────────────

def build_report(
    acquirer_data: dict,
    target_data: dict,
    acquirer_val: dict,
    target_val: dict,
    target_dcf: dict,
    premium: dict,
    flags: list,
    output_path: str,
) -> None:
    """Assemble and save the full PitchOS PDF by calling all render methods in order."""
    acq_ticker = acquirer_data.get("ticker", "ACQ")
    tgt_ticker  = target_data.get("ticker", "TGT")
    acq_name    = acquirer_data.get("company_name") or acq_ticker
    tgt_name    = target_data.get("company_name") or tgt_ticker

    # Merge DCF output and premium into one dict for sections that need both
    dcf_with_premium = {**target_dcf, **premium}

    pdf = PitchOSReport(acq_ticker, tgt_ticker)
    pdf.add_page()

    # Section order mirrors a standard pitch book structure
    pdf.render_company_snapshot(acquirer_data, "Acquirer")
    pdf.render_company_snapshot(target_data, "Target")
    pdf.render_valuation_section(acquirer_val, target_val, acquirer_data, target_data)
    pdf.render_dcf_section(target_dcf, premium)
    pdf.render_rationale_summary(acq_name, tgt_name, target_val, dcf_with_premium, flags)
    pdf.render_risk_flags(flags)

    pdf.output(output_path)
